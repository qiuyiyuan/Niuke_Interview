	public static int findMissing(int[][] numbers, int n) {
		int sum=0;
		for(int i=0;i<numbers.length;i++) {
			sum=0;
			for(int j=0;j<numbers[i].length;j++) {
				if(numbers[i][j]==1) {
					sum+=(StrictMath.pow(2,j));
				}
			}
			if(sum!=i) {
				return sum-1;
			}
		}
		return n;
    }
